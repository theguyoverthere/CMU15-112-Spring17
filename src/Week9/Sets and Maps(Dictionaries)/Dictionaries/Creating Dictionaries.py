# Create an empty dictionary
d = dict()
print(d)

# Create an empty dictionary with braces syntax
d = {}
print(d)

# Create a dictionary from a list of key value pairs
pairs = [("cows", 5), ("dog", 98), ("cat", 1)]
d = dict(pairs)
print(d)

# Statically allocate a dictionary
d = {"cows": 5, "dog":98, "cat":1}
print(d)
