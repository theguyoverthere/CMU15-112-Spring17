# Create an empty set
s = set()
print(s)

# Create a set from a list
s = set(['cat', 'dog', 'cow'])
print(s)

# Create a set from any iterable object
s = set("wahoo")
print(s)

# Create a statically allocated set
s = {2, 3, 5}
print(s)

# Caution: {} is not an empty set
s = {}
print(s)
print(type(s))

